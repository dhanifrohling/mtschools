		<h1 id="top">Chord Progressions</h1>
		<hr>
		<p>A chord progression is a series of organised <a href="chords">chords</a> which make the flesh of a piece. </p>
		<img src="content/chordprogressions/images/chord_progression.png">
		<p><b>Figure 1: </b>A basic chord progression over four bars.</p>
		<p>Although a song may use chord progressions containing only its diatonic chords, non-diatonic chords from a different <a href="keysignatures">keys</a> may be used to make a song sound more interesting.</p>
		<hr>
	</section>
