		<h2 id="circle_of_fifths">The Circle of Fifths</h2>
		<p>A way of identifying key signatures and which accidentals belong to each key is to study <b>the circle of fifths</b>:</p>
		<img src="content/keysignatures/images/circle_of_fifths.png">
		<p><b>Figure 2: </b>The glorious circle of fifths!</p>

		<p>To identify <b>how many sharps or flats</b> belong to each key, simply count left or right from C:

		<p><b>Left:</b> C = 0 flats, F = 1 flats, Bb = 2 flats, Eb = 3 flats, etc.</p>
		<p><b>Right:</b> C = 0 sharps, G = 1 sharps, D = 2 sharps, A = 3 sharps, etc.</p>

		<p>To identify <b>which sharps or flats</b> belong to each key, count left or right from in-between Bb and F:</p>
		<p>- For sharps you count right (F, C, G, D, A, E, B). A good mnemonic for remembering this order of sharps is <b>F</b>at <b>C</b>ats <b>G</b>o <b>D</b>own <b>A</b>lleys <b>E</b>ating <b>B</b>irds.</p>
		<p>- For flats you count left (B, E, A, D, G, C, F). A good mnemonic for remembering this order of flats is <b>B</b>attle <b>E</b>nds <b>A</b>nd <b>D</b>own <b>G</b>oes <b>C</b>harles' <b>F</b>ather.</p>

