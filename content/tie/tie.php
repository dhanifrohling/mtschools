		<h1 id="top">Tie</h1>
		<hr>
		<p>A tie (or bind) is a curved line placed under or over two <a href="notes">notes</a> of the same pitch to indicate one unbroken note with combined <a href="noteandrestvalues">time-values</a>.</p>
		<p>Although they look similar, a tie is not a <a href="slur">slur</a>!</p>
