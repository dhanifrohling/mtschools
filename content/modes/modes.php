	<section>
		<h1 id="top">Modes</h1>
		<hr>
		<p>Modes are alternate musical <a href="scales">scales</a> which derive from the common scales (major scale, harmonic minor, and melodic minor).</p>
		<p>They are built by starting on a degree of the common scales (inheriting the name 'tonic') and following the same sequence of steps until the new tonic is reached. Although the same sequence of steps is followed, the mode inherits a new distinct sound or mood.</p>
		<p>The origin of modal music is set back to medieval times and said to originate from the Ancient Greeks.</p>
