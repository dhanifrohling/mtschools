		<h1 id="top">Contours</h1>
		<hr>
		<p>A contour is the shape in which the pitch of a melody travels.</p>
		<p>There are four basic contours: <b>arch</b>, <b>wave</b>, <b>inverted arch</b>, and <b>pivotal</b>.</p>
	</section>
