	<section>
		<h2 id="hairpins">Hairpins</h2>
		<p>Hairpin are arrow shaped symbols which indicate the gradual increasing or decreasing of dynamic. There are two types of hairpins: <b>crescendo</b> and <b>diminuendo</b>.</p>
		<img src="content/dynamics/images/crescendo.png">
