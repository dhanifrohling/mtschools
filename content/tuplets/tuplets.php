		<h1 id="top">Tuplets</h1>
		<hr>

		<p>A tuplet is a group of <a href="notes">notes</a> with an irregular duration to what's permitted by the given <a href="timesignatures">time signature</a>.</p>
		<img src="content/tuplets/images/tuplet.png">
		<p><b>Figure 1: </b>A crotchet triplet.</p>

		<p>Every tuplet has a number written with it which indicates <b>how many irregular units the tuplet contains</b>, as well as <b>how many units of the regular rhythm the duplet will span for</b>:</p>
		<img src="content/tuplets/images/tuplet_spans_for.png">
		<p><b>Figure 2: </b>Our crotchet triplet spans for the duration of two regular crotchets.</p>

