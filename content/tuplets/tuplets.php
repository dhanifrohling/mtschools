	<section>
		<h2 id="ratios">Ratios</h2>
		<p>Sometimes a tuplet with an irregular ratio is required and can be written in place of the tuplet number.</p>
		<img src="content/tuplets/images/ratio_tuplet.png">
		<p><b>Figure 3: </b>A quaver quintuplet which spans for 8 regular quavers.</p>
		<p>When interpreting a tuplet with a ratio, the left and right numbers indicate:</p>
	<ul>
		<li type="circle"><b>Left number</b>: how many units the tuplet will contain; also gives the tuplet its appropriate name (e.g. duplet, triple, quadruplet, etc.)</li>
		<br />
		<li type="circle"><b>Right number</b>: how many units of the regular rhythm the tuplet will span for</li>
	</ul>
