		<h1 id="top">Slide</h1>
		<hr>
		<p>A slide is the passing from one <a href="notes">note</a> to another usually on stringed instruments. The fret-hand makes the application by sliding up or down a string.</p>
		<p>A tuba can also produce a satisfactory slide.</p>
