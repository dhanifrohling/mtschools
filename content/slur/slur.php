		<h1 id="top">Slur</h1>
		<hr>
		<p>A slur is a curved line used to group <a href="notes">notes</a> together indicating that they are to be played smoothly (legato).</p>
		<img src="content/slur/images/slurs.png">
		<p><b>Figure 1: </b>Three groups of notes slurred together.</p>
		<p>For string players, this indicates the slurred notes should be taken in one stroke of the bow or one pluck of the string. For wind players and singers it should be taken in one breath.</p>
		<p>If a note within a slur has a dot above it, this means it is to be played slightly detached.</p>
		<p>In vocal music, slurs are also used to indicate one syllable being sung over two or more notes.</p>
		<p>A slur is not to be confused with a <a href="tie">tie</a>!</p>
