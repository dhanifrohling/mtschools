		<p><b>Melodic Minor Scale</b></p>
		<p>The melodic minor scale shares the same series of steps as the natural minor scale with the exception of the 6th and 7th degrees, which are both raised a semitone while ascending. While descending, the 6th and 7th degree return to natural like in the natural minor scale.</p>
		<p>This rule of altering the intervals while descending is made to avoid an aug. 2nd, which can be awkward to sing by vocalists.</p>
