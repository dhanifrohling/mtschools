		<h2 id="major_pentatonic">Major Pentatonic Scale</h2>
		<p>The major pentatonic scale is a five note scale which consists of the 1st, 2nd, 3rd, 5th, and 6th degrees of the major scale.</p>
		<img src="content/scales/images/major_pentatonic.png">
		<p><b>Figure 6: </b>The C major pentatonic scale (T, T, Min. 3rd, T, Min. 3rd).</p>
		<hr>
	</section>
	<section>
		<h2 id="minor_pentatonic">Minor Pentatonic Scale</h2>
		<p>The minor pentatonic scale is a five note scale which consists of the 1st, 3rd, 4rd, 5th, and 7th degrees of the natural minor scale.</p>
		<img src="content/scales/images/minor_pentatonic.png">
		<p><b>Figure 7: </b>The A minor pentatonic scale (Min. 3rd, T, T, Min. 3rd, T).</p>
		<hr>
	</section>
	<section>
		<h2 id="blues">Blues Scale</h2>
		<p>The blues scale is the same as the minor pentatonic scale, but adds in a sharp 4 while ascending, and a flat 5 descending.</p>
		<img src="content/scales/images/blues_asc.png">
