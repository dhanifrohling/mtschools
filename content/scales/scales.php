	<section>
		<h1 id="top">Scales</h1>
		<hr>
		<p>A scale (It. for ladder or staircase) is a series of <a href="notes">notes</a> which progress up or down stepwise within an octave. Scales are used as the basis for musical composition and improvisation.</p>
		<p>Each scale has a set series of steps (<a href="interals">intervals</a>) which can be used to remember the scale itself. Another way of remembering a scale is to learn it's pattern when played on an instrument.</p>
		<hr>
	</section>
