	<section>
		<h1 id="top">Noteheads</h1>
		<hr>
		<p>Although a notehead usually indicates a pitch by it's position on the staff, different types of noteheads may also indicate a certain technique or instrument.</p>
		<hr>
	</section>
	<section>
		<h2 id="normal">Normal Notehead</h2>
		<p>A normal notehead imposes no special technique and is played naturally.</p>
		<img src="content/noteheads/images/normal.png">
		<p><b>Figure 1: </b>Five notes with normal noteheads.</p>
		<hr>
	</section>
	<section>
		<h2 id="cross">Cross Notehead</h2>
		<p>A cross notehead may indicate a note of uncertain pitch, usually for unpitched percussion. It may also indicate a muted note.</p>
		<img src="content/noteheads/images/cross.png">
		<p><b>Figure 2: </b>Five notes with cross noteheads.</p>
		<hr>
	</section>
	<section>
		<h2 id="diamond">Diamond Notehead</h2>
		<p>A diamond notehead usually indicates a note that is fingered but not played, such as a string harmonic. It may also indicate a piano key depressed silently.</p>
		<p>For guitar harmonics, diamond noteheads a crotchet or shorter are written with the diamond filled-in.</p>
		<img src="content/noteheads/images/diamond.png">
		<p><b>Figure 3: </b>Eight notes with diamond noteheads, some with filled-in diamonds.</p>
		<hr>
	</section>
	<section>
		<h2 id="beat">Beat Notehead</h2>
		<p>A beat note is written as a slash and indicates the rhythm of chord symbols. It is up to the interpreter to improvise the voicing of each chord played.</p>
		<p>There are two species of beat notes, ones with a stem and one without.</p>
		<p>They are usually written only on the middle line of the staff.</p>
		<img src="content/noteheads/images/beat.png">
		<p><b>Figure 4: </b>Six beat notes, some with a stem and some without.</p>
		<hr>
	</section>
	<section>
		<h2 id="headless">Headless Notehead</h2>
		<p>A headless note indicates purely a rhythm in contemporary music, either because a previous note or chord is being repeated, or because, like the cross notehead, the pitch is indefinite or improvised.</p>
		<p>Headless semibreves are hard to read.</p>
		<img src="content/noteheads/images/headless.png">
		<p><b>Figure 5: </b>Five headless notes with the semibreve being invisible.</p>
		<hr>
	</section>
	<section>
		<h2 id="stemless">Stemless Notehead</h2>
		<p>Stemless notes are useful for arrhythmic music such as plainchant.</p>
		<img src="content/noteheads/images/stemless.png">
		<p><b>Figure 6: </b>Two stemless notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="cue-size">Cue-Size Notehead</h2>
		<p>Cue-size notes are used to mix normal-sized and cue-sized notes within the same chord.</p>
		<img src="content/noteheads/images/cue-size.png">
		<p><b>Figure 7: </b>Five cue-size notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="slashed">Slashed Notehead</h2>
		<p>A notehead with a slash indicates a rim-shot in percussion notation.</p>
		<p>A slash or backslash may be used.</p>
		<img src="content/noteheads/images/slashed.png">
		<p><b>Figure 8: </b>Five notes with slashed noteheads.</p>
		<hr>
	</section>
	<section>
		<h2 id="arrow_down">Arrow Down Notehead</h2>
		<p>An arrow down notehead indicates an extremely low unspecified note. These notes are written without a ledger line.</p>
		<img src="content/noteheads/images/arrow_down.png">
		<p><b>Figure 9: </b>Five notes with arrow down noteheads.</p>
		<hr>
	</section>
	<section>
		<h2 id="arrow_up">Arrow Up Notehead</h2>
		<p>An arrow up notehead indicates an extremely high unspecified note. These notes are written without a ledger line.</p>
		<img src="content/noteheads/images/arrow_up.png">
		<p><b>Figure 10: </b>Five notes with arrow up noteheads.</p>
		<hr>
	</section>
	<section>
		<h2 id="shape">Shape Noteheads</h2>
		<p>Shape notes are used for shape note notation (also "sacred harp" music), in which each notes indicates a degree of the given scale.</p>
		<img src="content/noteheads/images/shape.png">
		<p><b>Figure 11: </b>The C Major scale in shape note notation.</p>
		<hr>
	</section>
	<section>
		<h2 id="large_cross">Large Cross Notehead</h2>
		<p>A large cross note is used in marching percussion, generally indicating all drums play in unison.</p>
		<img src="content/noteheads/images/large_cross.png">
		<p><b>Figure 12: </b>Two large cross notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="ping">Ping Notehead</h2>
		<p>A ping notehead is used in percussion notation which indicates a specific type of rim-shot.</p>
		<img src="content/noteheads/images/ping.png">
		<p><b>Figure 13: </b>Five notes with ping noteheads.</p>
