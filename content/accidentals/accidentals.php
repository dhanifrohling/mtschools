	<section>
		<h1 id="top">Accidentals</h1>
		<hr>
		<p>An accidental is a sign written before a <a href="notes">note</a> which raises or lowers it's pitch. The new pitch is held till the end of the <a href="bar">bar</a> unless written otherwise.</p>
		<p>Accidentals may be bracketed as a reminder.</p>
		<hr>
	</section>
	<section>
		<h2 id="sharp">Sharp</h2>
		<p>A sharp raises the pitch of a note by a semitone.</p>
		<img src="content/accidentals/images/sharp.png">
		<p><b>Figure 1: </b>Two sharp notes (G#, D#).</p>
		<hr>
	</section>
	<section>
		<h2 id="flat">Flat</h2>
		<p>A flat lowers the pitch of a note by a semitone.</p>
		<img src="content/accidentals/images/flat.png">
		<p><b>Figure 2: </b>Two flat notes (Gb, Db).</p>
		<hr>
	</section>
	<section>
		<h2 id="natural">Natural</h2>
		<p>A natural strips the note of any accidental alteration and restores it to its natural pitch.</p>
		<img src="content/accidentals/images/natural.png">
		<p><b>Figure 3: </b>Two natural notes (G, D).</p>
		<hr>
	</section>
	<section>
		<h2 id="double-sharp">Double-Sharp</h2>
		<p>A double-sharp raises the pitch of a note a tone, double that of a regular sharp.</p>
		<img src="content/accidentals/images/double-sharp.png">
		<p><b>Figure 4: </b>Two double-sharp notes (G##, D##).</p>
		<hr>
	</section>
	<section>
		<h2 id="double-flat">Double-Flat</h2>
		<p>A double-flat lowers the pitch of a note a tone, double that of a regular flat.</p>
		<img src="content/accidentals/images/double-flat.png">
		<p><b>Figure 5: </b>Two double-flat notes (Gbb, Dbb).</p>
