		<h1 id="top">Tremolos</h1>
		<hr>
		<p>Tremolos are a musical shorthand which indicate the rapid repetition of <a href="notes">notes</a> and <a href="chords">chords</a>.</p>
		<p>There are two types of tremolos:
		<p><b>1.</b> The repetition of the same note</a> or chord, notated with strokes through the stem (or where the stem would've taken place for stemless notes)</p>
		<p><b>2.</b> The alternating repetition of two different notes or chords, notated with strokes between the two figures.</p>
		<img src="content/tremolos/images/tremolos.png">
		<p><b>Figure 1: </b>An array of different tremolos.</p>
		<p>Tremolos are not to be mixed up with <a href="vibrato">vibrato</a> (the wavering/oscillation of pitch)!</p>
		<hr>
	</section>
