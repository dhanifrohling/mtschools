	<section>
		<h2 id="buzz_roll">Buzz Roll</h2>
		<p>A buzz roll is a special kind of tremolo used in percussion notation which indicates to repeatedly strike the note or chord as fast as possible.</p> 		<p>Buzz rolls are notated with a Z through the stem.</p>
		<img src="content/tremolos/images/buzz_roll.png">
		<p><b>Figure 7: </b>Three different buzz rolls.</p>
