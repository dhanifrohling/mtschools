	<section>
		<h1 id="top">Intervals</h1>
		<hr>
		<p>A musical interval is the distance of pitch between two <a href="notes">notes</a>. They may be described as harmonic (the two notes heard simultaneously) or melodic (proceeding each other).</p>
		<img src="content/intervals/images/sixth.png">
		<p><b>Figure 1: </b>The interval Minor 6th.</p>
		<p>To measure an interval's size, two factors are taken into account: <b>quantity</b> and <b>quality</b>.</p>
		<hr>
	</section>
	<section>
		<h2 id="quantity">Quantity</h2>
		<p>An intervals quantity is written numerically (e.g. C to G is a 5th).</p>
		<p>To identify an intervals quantity, simply add up the number of lines and spaces between the two notes, including the lines and spaces containing the notes themselves. For example:</p>
		<img src="content/intervals/images/quantity.png">
