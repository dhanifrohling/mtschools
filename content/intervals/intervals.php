	<section>
		<h2 id="compound">Compound Intervals</h2>
		<p>Compound intervals are those greater than an octave in length. They may increase their numeric quantity greater than 8 (e.g. maj. 9th), or simply be called a compound maj. 2nd.</p>
		<img src="content/intervals/images/compound.png">
		<p><b>Figure 6: </b>A maj. 9th or compound maj. 2nd.</p>
