		<p>An interval six semitones in length is called a tritone, which is the same length as an aug. 4th or dim. 5th.</p>
		<p><a href="accidentals">Accidentals</a> may be used to sharpen or flatten either note of an interval and give it a new quality:</p>
		<img src="content/intervals/images/compare_quality.png">
		<p><b>Figure 4: </b>Five intervals with the same quantity, but different qualities.</p>
		<p>When accidentals are used to alter an intervals quality, the following four rules are implied:</p>
		<p>
	<b>First rule</b>: If any major interval is flattened by a semitone, its quality becomes minor.<br />
	<b>Second rule</b>: If any minor interval is sharpened by a semitone, it becomes major.<br />
	<b>Third rule</b>: If any major or perfect interval is sharpened by a semitone, it becomes augmented.<br />
	<b>Fourth rule</b>: If any minor or perfect interval is flattened by a semitone, it becomes diminished.
</p>
