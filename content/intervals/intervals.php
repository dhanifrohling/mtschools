		<h2 id="quality">Quality</h2>
		<p>Interval quality is used to describe interval quantity (e.g. Major 3th). It's what gives an interval it's distinct sound.</p>
		<p>While quantity is known by counting the number of lines and spaces written, quality requires counting the full amount of semitones from the first to second note. For example:</p>
		<img src="content/intervals/images/quality.png">
		<p><b>Figure 3: </b>There are four semitones from F to A (F, F#, G, G#, A) which makes a Major 3rd.</p>
		<p>An interval's quality may be described as one of the following terms:</p>
		<p>
	- Major (maj.)<br />
	- Minor (min.)<br />
	- Perfect (perf.)<br />
	- Augmented (aug.)<br />
	- Diminished (dim.)
</p>
		<p>Because some intervals sound more harmonious than others, and others more perfected, it is categorized that 2nd's, 3rd's, 6th's and 7th's may be major or minor and unison's, 4th's, 5th's and octave's may be perfect.</p>
