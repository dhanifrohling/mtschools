	<section>
		<h1 id="top">Repeats</h1>
		<hr>
		<p>Repeats are various ways to navigate a composition through symbols, <a href="barlines">barlines</a>, and repeat marks.</p>
		<hr>
	</section>
	<section>
		<h2 id="repeat_bar_symbols">Repeat Symbols</h2>
		<p>A repeat symbol simply indicates to repeat the previous bar.</p>
		<img src="content/repeats/images/repeat.png">
		<p><b>Figure 1: </b>The previous bar repeated.</p>
		<p>Different versions of the symbol are used to repeat the previous two or four bars:</p>
		<img src="content/repeats/images/two_repeat.png">
		<p><b>Figure 2: </b>The previous two bars repeated.</p>
		<img src="content/repeats/images/four_repeat.png">
		<p><b>Figure 3: </b>The previous four bars repeated.</p>
		<hr>
	</section>
	<section>
		<h2 id="barlines">Repeat Section</h2>
		<p>A section of a song may be repeated by using repeat barlines to indicate the beginning and ending of the repeat.</p>
		<img src="content/repeats/images/barlines.png">
		<p><b>Figure 4: </b>A section repeated using repeat barlines.</p>
		<hr>
	</section>
	<section>
		<h2 id="endings">Repeat Endings</h2>
		<p>Repeat endings are lines placed above the <a href="staff">staff</a> which indicate the first, second, third, etc. course of music during the end of a repeated section.</p>
		<img src="content/repeats/images/endings.png">
		<p><b>Figure 5: </b>A repeat section with different endings.</p>
		<hr>
	</section>
	<section>
		<h2 id="coda">Repeat Marks</h2>
		<p>A repeat mark indicates a specific instruction to navigate a song (e.g. <b>D.S. al Coda</b>).</p>
		<p>Before learning repeat marks, there are two symbols to learn which may be placed at any point of a song:</p>
		<table id="content-table">
		<tr>
			<th>Symbol</th>
			<th>Name</th>
		</tr>
		<tr>
			<td class="center-cell"><img src="content/repeats/images/coda.png" /></td>
			<td>Coda</td>
		</tr>
		<tr>
			<td class="center-cell"><img src="content/repeats/images/segno.png" /></td>
			<td>Segno</td>
		</tr>
	</table>
		<p>There are four different repeat marks (all written in Italian):</p>
	<table id="content-table">
		<tr>
			<th>Repeat Mark</th>
			<th>Instruction</th>
		</tr>
		<tr>
			<td><b>D.C. al Fine</b></td>
			<td>Go back to the <b>beginning</b> and end the song at <b>"Fine"</b></td>
		</tr>
		<tr>
			<td><b>D.C. al Coda</b></td>
			<td>Go back to the <b>beginning</b> and play to <b>"To Coda</b>,<b>"</b> then skip and play from the <b>coda symbol</b></td>
		</tr>
		<tr>
			<td><b>D.S. al Fine</b></td>
			<td>Go back to the <b>segno symbol</b> and end the song at <b>"Fine"</b></td>
		</tr>
		<tr>
			<td><b>D.S. al Coda</b></td>
			<td>Go back to the <b>segno symbol</b> and play to <b>"To Coda</b>,<b>"</b> then skip and play from the <b>coda symbol</b></td>
		</tr>
	</table>
