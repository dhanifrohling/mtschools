		<h1 id="top">Phrase</h1>
		<hr>
		<p>A musical phrase is a short section of music into which the music seems to naturally fall. Often a phrase is four <a href="bar">bars</a> long, but shorter and longer ones may occur.</p>
		<p>It is an inexact term: sometimes a phrase may be contained within one breath, and sometimes they are subdivided by <a href="slur">slurs</a> to indicate their proper punctuation in performance.</p>
		<p>The art of phrasing by a performer, whether conductor, singer, or instrumentalist, is often instinctive and is one of the features by which an accomplished performer may be distinguished from one less skilled.</p>
