	<section>
		<h2 id="second">Second Species</h2>
		<p>In the second species, there are two minims (half notes) against one semibreve (whole note).</p>
		<p>The same set of rules applies from the first species unless mentioned below:</p>
	<ul>
		<li>It is permissible to begin with a minim rest, starting the counterpoint on the upbeat (which must be a perfect consonance)</li>
		<li>The downbeat must be a consonant</li>
		<li>The upbeat may be a dissonant if it moves from the proceeding note and to the next note stepwise</li>
		<li>If the upbeat moved by skip, it must be a consonant</li>
		<li>The skip of a third cannot prevent the succession of two fifths or octaves on the downbeats</li>
		<li>The skip of a minor sixth or an octave is permitted if there is no possibility of contrary motion and the two parts have been led so close together that one does not know where to take them</li>
		<li>If the cantus firmus is in the lower part, the second last bar should consist of a fifth followed by a major sixth</li>
		<li>If the cantus firmus is in the upper part, the second last bar should consist of a fifth followed by a minor third</li>
	</ul>
