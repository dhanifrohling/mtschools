	<section>
		<h2 id="fourth">Fourth Species</h2>
		<p>In the fourth species, there are two minims (half notes) against a semibreve (whole note), and the minims are tied together over each barline. This species is also known as ‘ligature’ or ‘syncopation.’</p>
		<p>The same sets of rules apply from the previous species unless mentioned below:</p>
	<ul>
		<li>Every bar can be consonant or dissonant:</li>
		<ul>
			<li>Consonant bar: Both minims are consonant intervals</li>
			<li>Dissonant bar: The downbeat is a consonant and the upbeat a dissonant</li>
		</ul>
		<li>Dissonant intervals must always be resolved descending stepwise to next consonance</li>
		<li>One may replace ligature with an untied minims anywhere ligature cannot be introduced. The ligature must start up again whenever the opportunity rises</li>
	</ul>
		<p><i><u>If the cantus firmus occurs in the lower voice:</u></i></p>
	<ul>
		<li>The interval of a third on the upbeat followed by a second on the downbeat is permitted</li>
		<li>The interval of a tenth on the upbeat followed by a ninth on the downbeat is permitted</li>
		<li>The second last bar should consist of a seventh followed by a sixth</li>
	</ul>
		<p><i><u>If the cantus firmus occurs in the upper voice:</u></i></p>
	<ul>
		<li>One may use from the downbeat: a second to the third, a fourth to the fifth, or a ninth to the tenth</li>
		<li>A seventh to the octave is not permitted, and the seventh may be omitted with a minim rest</li>
		<li>The second last bar should consist of a second followed by a third, followed by unison in the final bar</li>
	</ul>
