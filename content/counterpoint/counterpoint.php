		<h1 id="top">Counterpoint</h1>
		<hr>
		<p>Counterpoint (pl. contrapuntist) is the study of two or more individual lines of music written above each other and played simultaneously.</p>
		<p>The art of counterpoint has been developed from as early as the 9th century, and since it has come to be categorized into five different species, each with their own set of rules.</p>
		<p>But before studying the different species, one must learn the difference between consonant and dissonant <a href="intervals">intervals</a> and the rules of motion.</p>
		<hr>
	</section>
