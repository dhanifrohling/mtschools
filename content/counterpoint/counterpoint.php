	<section>
		<h2 id="motions">Motions</h2>
		<p>Motion is the study of linear movement between two or more lines of music. There are three different types of motion: <b>direct</b>, <b>contrary</b>, and <b>oblique</b>.</p>
		<p><b><u>Direct Motion</u></b></p>
		<p>Direct motion results when both lines of music move in the same direction.</p>
		<img src="content/counterpoint/images/direct.png">
		<p><b><u>Contrary Motion</u></b></p>
		<p>Contrary motion is when one line of music moves in one direction and the other to the contrary.</p>
		<img src="content/counterpoint/images/contrary.png">
		<p><b><u>Oblique Motion</u></b></p>
		<p>Oblique motion is when one line of music remains stationary, and the other moves up or down by step or skip.</p>
		<img src="content/counterpoint/images/oblique.png">
		<p>When using these motions, it is important to follow the following set of rules:</p>
	<ul>
		<li><i><u>First rule:</u></i> From one perfect consonance to another perfect consonance one must proceed in contrary or oblique motion</li>
		<li><i><u>Second rule:</u></i> From a perfect consonance to an imperfect consonance one may proceed in any of the three motions</li>
		<li><i><u>Third rule:</u></i> From an imperfect consonance to a perfect consonance one must proceed in contrary or oblique motion</li>
		<li><i><u>Fourth rule:</u></i> From one imperfect consonance to another imperfect consonance one may proceed in any of the three motions</li>
	</ul>
		<p>Usually, before beginning to write counterpoint, a cantus firmus (a set melody) is required as the basis to which the composer adds to.</p>
			<hr>
	</section>
	<section>
		<h2 id="first">First Species</h2>
		<p>In the first species of counterpoint, both lines of music consist only of semibreves (whole notes).</p>
		<p>The following is a list of rules for the first species:</p>
	<ul>
		<li>Consonant intervals are only permitted</li>
		<li>If the cantus firmus is in the lower part, the first interval may be a unison, fifth, or octave</li>
		<li>If the cantus firmus is in the upper part, the first interval must be a unison or octave</li>
		<li>The last interval may only be a unison or an octave</li>
		<li>More imperfect than perfect consonances should be used in the body</li>
		<li>Unison cannot be used in the body</li>
		<li>A tritone is not permitted (diminished fifth/augmented fourth)</li>
		<li>Contrary and oblique motion should be used as much as possible</li>
		<li>A skip of a major sixth is prohibited</li>
		<li>A tenth into an octave by contrary motion is prohibited at the beginning of a bar</li>
		<li>A consonance into an octave by skip is prohibited</li>
		<li>If the cantus firmus is in the lower part, the second last interval should be a major sixth</li>
		<li>If the cantus firmus is in the upper part, the second last interval should be a minor third</li>
	</ul>
