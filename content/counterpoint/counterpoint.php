	<section>
		<h2 id="fifth">Fifth Species</h2>
		<p>The fifth species of counterpoint, rather than a strict number of notes against each other, is the combination of all previous species to make a unique melodic line. This species is also known as ‘florid counterpoint’ as its composition should flourish and make a line of excellences.</p>
		<p>There are only a few new points of order for this species:</p>
	<ul>
		<li>Great use of oblique motion or ligature should be made when approaching the downbeats</li>
		<li>Although passable, two crotchets (quarter notes) at the beginning of a bar without a ligature following is considered weak counterpoint</li>
	</ul>
