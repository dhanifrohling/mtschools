	<section>
		<h2 id="third">Third Species</h2>
		<p>In the third species, there are four crotchets (quarter notes) against one semibreve (whole note).</p>
		<p>The same sets of rules apply from the previous species unless mentioned below:</p>
	<ul>
		<li>If four crotchets in a bar follow each other ascending or descending stepwise, the first one has to be a consonant, the second may be a dissonant, and the third must again be a consonant. The fourth one may be a dissonant if the fifth note (in the new bar) is a consonant</li>
		<li>The previous point does not hold if the second and fourth notes are consonant, in which case the third is always a dissonant</li>
		<li>Another alternative to the first point is ‘cambiata’ - beginning with a consonant, the second a dissonant, and the third a consonant by the skip of a third</li>
