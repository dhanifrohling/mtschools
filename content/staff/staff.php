		<h1 id="top">Staff</h1>
		<hr>
		<p>A staff (pl. staves) is the system of five parallel lines which <a href="notes">notes</a> are written on and between.</p>
		<p>The pitch range of a staff is determined by a <a href="clefs">clef</a> which is usually written at the beginning of each musical <a href="system">system</a>.</p>
		<p>In American parlance a staff is called a stave.</p>
		<img src="content/staff/images/staff_with_treble_clef.png">
		<p><b>Figure 1: </b>A basic staff with a treble clef.</p>
		<hr>
	</section>
	<section>
		<h2 id="great_staff">Great Staff</h2>
		<p>A great staff (also grand staff) is a notational device where two staves (one with treble clef and the other bass clef) are brought together and connected by a vertical <a href="brace">brace</a>. Between the two staves is the <a href="ledgerline">ledger line</a> Middle C.</p>
		<p>A great staff is commonly used for piano and harp notation.</p>
		<img src="content/staff/images/great_staff.png">
