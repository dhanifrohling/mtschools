		<h1 id="top">Vibrato</h1>
		<hr>
		<p>Vibrato is a line placed over the <a href="staff">staff</a> which indicates the rapid and slight variation of a <a href="notes">notes</a> pitch.</p>
		<p>It is produced on string instruments by vibrating the fingers of the fretted-hand, and on wind instruments and singers by breath-control.</p>
		<img src="content/vibrato/images/vibrato.png">
		<p><b>Figure 1: </b>Three notes with vibrato.</p>
