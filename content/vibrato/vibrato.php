	</section>
	<section>
		<h2 id="wide">Wide Vibrato</h2>
		<p>Wide vibrato vibrates the pitch to as wide as a whole tone. This technique is commonly adopted by electric-guitar players.</p>
		<img src="content/vibrato/images/wide_vibrato.png">
