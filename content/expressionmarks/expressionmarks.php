	<section>
		<h1 id="top">Expression Marks</h1>
		<hr>
		<p>Expression marks are short music terms written at the beginning of a score to indicate a kind of mood or <a href="tempo">tempo</a> to be interpreted. They may also be written anywhere a new mood or tempo is to occur.</p>
		<p>Although similar, expression marks are not to be confused with <a href="tempo#tempo_notation">tempo notation</a>!</p>
		<img src="content/expressionmarks/images/expression_mark.png">
		<p><b>Figure 1: </b>An expression mark at the beginning of a score.</p>
		<hr>
	</section>
