	<section>
		<h1 id="top">Beam</h1>
		<hr>

		<p>A beam is a thick horizontal line which links the <a href="notes#tail">tails</a> of two or more <a href="notes">notes</a>.</p>
		<img src="content/beam/images/beam.png">
		<p><b>Figure 1: </b>Two quavers beamed together.</p>

		<p>Notes are beamed together according to the musical context (meter). For example, in 4/4 time you could group a bar of quavers as followed:</p>
		<img src="content/beam/images/quavers_beamed_1.png">
		<img src="content/beam/images/quavers_beamed_2.png">
		<img src="content/beam/images/quavers_beamed_3.png">
		<p><b>Figure 2: </b>Three bars of quavers, each beamed differently.</p>
		<p>Beams are written above or below the group of notes depending on the greater number of pitches written above or below the centre-line:</p>
		<img src="content/beam/images/beam_above_notes.png">
		<p><b>Figure 3: </b>A group of notes with it's beam written above.</p>
		<img src="content/beam/images/beam_below_notes.png">
		<p><b>Figure 4: </b>A group of notes with it's beam written below.</p>
		<p>Beams may also be angled according to the <a href="contours">contour</a> of the grouped notes:</p>
		<img src="content/beam/images/angled_beam.png">
		<p><b>Figure 5: </b>An angled beam.</p>
		<p>In some music (i.e. percussion notation) it is customary for beams to always be drawn horizontal and never on an angle.</p>
		<p>Beams are split up into two different layers: <b>primary</b> and <b>secondary</b>.</p>
		<hr>
	</section>
	<section>
		<h2 id="primary_and_secondary">Primary and Secondary Beams</h2>
		<p>The primary beam is the one furthest from the noteheads whereas secondary beams are any beam in-between:</p>
		<img src="content/beam/images/primary_and_secondary_beams.png">
		<p><b>Figure 6: </b>The primary and secondary beams outlined in a bar of notes.</p>
		<p>This method of beaming allows the rhythm to be read clearly.</p>
		<hr>
	</section>
	<section>
		<h2 id="between_notes">Beam Between Notes</h2>
		<p>In older scores (particularly in violin notation), if a group of notes are very low then very high (or vice versa), you will sometimes see the beam running between the notes:</p>
		<img src="content/beam/images/beam_between_notes.png">
		<p><b>Figure 7: </b>A beam between notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="across_barlines">Beam Across Barlines</h2>
		<p>Beams may carry on from one bar to the next as shown in this bass clarinet part of Stravinsky's <i>Petrouchka</i>:</p>
		<img src="content/beam/images/beam_across_barlines.png">
		<p><b>Figure 8: </b>A beam running across a barline (even a system).</p>
		<hr>
	</section>
	<section>
		<h2 id="cross-staff">Cross-Staff Beam</h2>
		<p>In keyboard and harp notation, notes from both staves may be beamed together to indicate a flowing between the hands:</p>
		<img src="content/beam/images/cross_staff_beams.png">
		<p><b>Figure 9: </b>Beaming between the great staff.</p>
