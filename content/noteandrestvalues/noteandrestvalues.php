	<section>
		<h1 id="top">Note & Rest Values</h1>
		<hr>
		<p>A <a href="notes">note</a> or <a href="rests">rest's</a> rhythmic duration is determined by its shape and/or components (<a href="notes#notehead">notehead</a>, <a href="notes#stem">stem</a>, and <a href="notes#tail">tail</a>).</p>
		<p>The British name-set originates from old Latin in the early Middle Ages whereas the American name-set is purely arithmetical.</p>
		<p>To understand how notes and rests fit into a <a href="bar">bar</a>, an understanding of <a href="timesignatures">time signatures</a> is necessary.</p>
		<hr>
	</section>
