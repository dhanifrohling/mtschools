	<section>
		<h1 id="top">Ornaments</h1>
		<hr>
		<p>An ornament is a small <a href="notes">note</a> or symbol which imposes an embellishment on a note or <a href="chords">chord</a>.</p>
		<hr>
	</section>
	<section>
		<h2 id="appoggiatura">Appoggiatura</h2>
		<p>An appoggiatura is a species of grace note. Placed before a given note, it leans in and takes a certain amount of time.</p>
		<p>Placed before an regular note, it usually takes half the time value:</p>
		<img src="content/ornaments/images/appoggiatura_before_normal_note.png">
		<p><b>Figure 1: </b>An appoggiatura before a regular note.</p>
		<p>Placed before a dotted note, it usually takes two-thirds of the time value:</p>
		<img src="content/ornaments/images/appoggiatura_before_dotted_note.png">
		<p><b>Figure 2: </b>An appoggiatura before a dotted note.</p>
		<p>Placed before two tied notes, it usually takes the whole value of the first note:</p>
		<img src="content/ornaments/images/appoggiatura_before_tied_notes.png">
		<p><b>Figure 3: </b>An appoggiatura before two tied notes.</p>
		<p>If an appoggiatura leans upon a chord, only one note is affected:</p>
		<img src="content/ornaments/images/appoggiatura_before_chord.png">
		<p><b>Figure 4: </b>An appoggiatura before a chord.</p>
		<hr>
	</section>
	<section>
		<h2 id="acciaccatura">Acciaccatura</h2>
		<p>An acciaccatura is a species of grace note, written with a line through its stem.</p>
		<p>Placed before a given note, it indicates to be crushed as quickly as possible, leaving the principal note with almost all time value.</p>
		<img src="content/ornaments/images/acciaccatura.png">
		<p><b>Figure 5: </b>Two acciaccaturas.</p>
		<p>An acciaccatura before a chord indicates to spread the chord:</p>
		<img src="content/ornaments/images/acciaccatura_with_chord.png">
		<p><b>Figure 6: </b>An acciaccatura before a chord.</p>
		<hr>
	</section>
	<section>
		<h2 id="trill">Trill</h2>
		<p>A trill is a line placed above the staff which indicates the rapid alteration between a given note, and the diatonic note above. The alternating notes are slurred together and played legato.</p>
		<img src="content/ornaments/images/trill.png">
		<p><b>Figure 7: </b>One bar of trill.</p>
		<hr>
	</section>
	<section>
		<h2 id="mordent">Mordent</h2>
		<p>A mordent is a symbol which indicates to crush two notes in before a given note to produce a short trill effect.</p>
		<p>There are two types of mordent: <b>upper mordent</b> and <b>lower mordent</b>.</p>
		<p>An upper mordent has the middle note higher than the principal note:</p>
		<img src="content/ornaments/images/upper_mordent.png">
		<p><b>Figure 8: </b>An upper mordent.</p>
		<p>A lower mordent has the middle note lower than the principal note:</p>
		<img src="content/ornaments/images/lower_mordent.png">
		<p><b>Figure 9: </b>A lower mordent.</p>
		<p>The middle note of a mordent may be altered in pitch by adding an accidental above the symbol:</p>
		<img src="content/ornaments/images/mordent_with_accidental.png">
		<p><b>Figure 10: </b>An upper mordent with a sharp.</p>
		<hr>
	</section>
	<section>
		<h2 id="gruppetto">Gruppetto</h2>
		<p>A gruppetto (or turn) is a symbol which implements a four-note figure after or before a given note depending on whether the sign is placed after or over it:</p>
		<img src="content/ornaments/images/gruppetto_between_notes.png">
		<p><b>Figure 11: </b>A gruppetto after the principal note.</p>
		<img src="content/ornaments/images/gruppetto_on_note.png">
		<p><b>Figure 12: </b>A gruppetto over the principal note.</p>
		<p>The pivot note of a gruppetto may be altered in pitch by adding an accidental above or below the symbol:</p>
		<img src="content/ornaments/images/gruppetto_with_flat.png">
		<p><b>Figure 13: </b>A gruppetto with a flat.</p>
		<img src="content/ornaments/images/gruppetto_with_sharp.png">
		<p><b>Figure 14: </b>A gruppetto with a sharp.</p>
		<img src="content/ornaments/images/gruppetto_with_natural.png">
		<p><b>Figure 15: </b>A gruppetto with a natural.</p>
