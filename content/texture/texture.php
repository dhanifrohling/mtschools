		<h1 id="top">Texture</h1>
		<hr>
		<p>Musical texture is a term used to describe the vertical aspects (layering) of a work or passage.</p>
		<p>There are three basic musical textures: <b>monophony</b>, <b>polyphony</b>, and <b>homophony</b>.</p>
		<hr>
	</section>
	<section>
		<h2 id="monophony">Monophony</h2>
		<p>Monophony is a single line of music without any harmonic or melodic <a href="counterpoint">counterpoint</a>.</p>
		<p>Monophony is considered the oldest type of music originating from Ancient Greece and early church music (i.e. plainchant/Gregorian chant).</p>
		<img src="content/texture/images/monophony.png">
		<p><b>Figure 1: </b>Monophony: a single line of music.</p>
		<hr>
	</section>
	<section>
		<h2 id="polyphony">Polyphony</h2>
		<p>Polyphony describes music with two or more unique <a href="voice">voices</a> played simultaneously.</p>
		<img src="content/texture/images/polyphony.png">
		<p><b>Figure 2: </b>Polyphony: two lines of music played against each other.</p>
		<hr>
	</section>
	<section>
		<h2 id="homophony">Homophony</h2>
		<p>Homophony describes a single melodic line with other parts acting as accompaniment.</p>
		<img src="content/texture/images/homophony.png">
		<p><b>Figure 3: </b>Homophony over four staves.</p>
