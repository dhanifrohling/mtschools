		<h2 id="tempo_notation">Tempo Notation</h2>
		<p>Tempo notation is a vocabulary of terms used to generally describe the pace of a song.</p>
		<p>They are all in Italian simply because they came into use around 1600-1750, a time when the bulk of European music came from Italian composers.</p>
		<img src="content/tempo/images/tempo_notation.png">
		<p><b>Figure 2: </b>A tempo notation mark written at the beginning of a piece.</p>
		<p>The following is a list of tempo notation marks with their corresponding tempos:</p>
		<table>
			<tr>
				<th>Tempo Notation Mark</th>
				<th>Description</th>
			</tr>
			<tr>
				<td>Grave</td>
				<td>The slowest pace. Very formal and very, very slow.</td>
			</tr>
			<tr>
				<td>Largo</td>
				<td>Funeral march slow. Very serious and somber.</td>
			</tr>
			<tr>
				<td>Larghetto</td>
				<td>Slow, but not as slow as largo.</td>
			</tr>
			<tr>
				<td>Lento</td>
				<td>Slow.</td>
			</tr>
			<tr>
				<td>Adagio</td>
				<td>Leisurely. Think graduation and wedding marches.</td>
			</tr>
			<tr>
				<td>Andante</td>
				<td>Walking pace. Close to the original minim.</td>
			</tr>
			<tr>
				<td>Andantio</td>
				<td>Slightly faster than andante. Think Pasty Cline's "Walking After Midnight," or any other lonely cowboy ballad you can think of.</td>
			</tr>
			<tr>
				<td>Moderato</td>
				<td>Right smack in the middle. Not fast or slow, just moderate.</td>
			</tr>
			<tr>
				<td>Allegretto</td>
				<td>Moderately fast.</td>
			</tr>
			<tr>
				<td>Allegro</td>
				<td>Quick, brisk, galloping along.</td>
			</tr>
			<tr>
				<td>Vivace</td>
				<td>Lively, fast.</td>
			</tr>
			<tr>
				<td>Presto</td>
				<td>Very fast.</td>
			</tr>
			<tr>
				<td>Prestissimo</td>
				<td>Think "Flight of the Bumblebee."</td>
			</tr>
		</table>
