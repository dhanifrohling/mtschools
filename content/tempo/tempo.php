		<h1 id="top">Tempo</h1>
		<hr>
		<p>Tempo is the speed at which a piece of music is performed. It is measured in beats per minute (BPM).</p>
		<p>A tempo marking is usually placed at the beginning of a piece or anywhere a new tempo is to occur.</p>
		<img src="content/tempo/images/tempo_marking.png">
		<p><b>Figure 1: </b>A tempo marking written at the beginning of a piece.</p>
		<hr>
	</section>
