	<section>
		<h1 id="top">Time Signatures</h1>
		<hr>
		<p>A time signature is a sign written at the beginning of a song (after the <a href="clefs">clef</a> and <a href="keysignatures">key signature</a>) which indicates the time and meter to be played.</p>
		<p>They are also written anywhere a new time or meter is to occur.</p>
		<img src="content/timesignatures/images/basic_time_signature.png">
		<p><b>Figure 1: </b>A basic time signature.</p>
		<p>Time signatures contains two numbers; one above the other. The bottom being the <b>denominator</b> and the top the <b>numerator</b>.</p>
		<hr>
	</section>
