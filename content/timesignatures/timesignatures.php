	<section>
		<h2 id="numerator">Numerator</h2>
		<p>The numerator (<b>top number</b>) simply indicates how many of the selected denominator per bar.</p>
		<p>To master these two factors is to master the basics of time signatures:</p>
		<img src="content/timesignatures/images/calculated.png">
		<p><b>Figure 2: </b>Three different time signatures with their bars complete.</p>
		<p>Time signatures are divided into three categories: <b>simple time</b>, <b>compound time</b>, and <b>irregular time</b>.</p>
		<hr>
	</section>
