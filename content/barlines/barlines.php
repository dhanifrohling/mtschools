	<section>
		<h1 id="top">Barlines</h1>
		<hr>
		<p>Barlines are vertical lines written through the <a href="staff">staff</a> which indicate the separation of <a href="bar">bars</a>, the end of a section, end of a work, or the beginning and ending of a repeated section.</p>
		<hr>
	</section>
	<section>
		<h2 id="normal">Normal Barline</h2>
		<p>A normal barline indicates the beginning and ending of a bar.</p>
		<img src="content/barlines/images/normal_barline.png">
		<p><b>Figure 1: </b>A normal barline.</p>
		<hr>
	</section>
	<section>
		<h2 id="double">Double Barline</h2>
		<p>A double barline indicates the end of a section.</p>
		<img src="content/barlines/images/double_barline.png">
		<p><b>Figure 2: </b>A double barline.</p>
		<p>They are also written before a <a href="keysignatures">key signature</a> change.</p>
		<hr>
	</section>
	<section>
		<h2 id="final">Final Barline</h2>
		<p>A final barline indicates the end of a song or movement. They look like a double barlines, but with a thicker right line.</p>
		<img src="content/barlines/images/final_barline.png">
		<p><b>Figure 3: </b>A final barline.</p>
		<hr>
	</section>
	<section>
		<h2 id="start_repeat">Start Repeat Barline</h2>
		<p>A start repeat barline indicates the beginning of a repeated section. The sections length is determined by the location of the next end repeat barline.</p>
		<img src="content/barlines/images/start_repeat_barline.png">
		<p><b>Figure 4: </b>A start repeat barline.</p>
		<p>Repeat barlines may be winged, which is common in handwritten and jazz music.</p>
		<hr>
	</section>
	<section>
		<h2 id="end_repeat">End Repeat Barline</h2>
		<p>An end repeat barline indicates the end of a repeated section. The sections length is determined by the previous start repeat barline.</p>
		<img src="content/barlines/images/end_repeat_barline.png">
		<p><b>Figure 5: </b>An end repeat barline.</p>
		<p>Repeat barlines may be winged, which is common in handwritten and jazz music.</p>
		<hr>
	</section>
