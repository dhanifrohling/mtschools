		<h1 id="top">Clefs</h1>
		<hr>
		<p>A clef (Lat. clavis: key) is a symbol written at the beginning of each line of music which determine the pitch-range of the staff.</p>
		<p>A clef may also be written at any point a new pitch-range is to begin.</p>
		<hr>
	</section>
	<section>
		<h2 id="treble">Treble Clef</h2>
		<p>The treble clef (also G clef) looks like a stylised 'G' and places the pitch G4 on the second line from the bottom. It is used for high-pitched instruments, high-voices, and right-hand piano parts.</p>
		<p>Two great mnemonics for remembering the treble clef are: <b>E</b>very <b>G</b>ood <b>B</b>oy <b>D</b>eserves <b>F</b>ruit and simply <b>FACE</b>.</p>
		<img src="content/clefs/images/treble_clef.png">
		<p><b>Figure 1: </b>A treble clef with pitches written.</p>
		<hr>
	</section>
	<section>
		<h2 id="bass">Bass Clef</h2>
		<p>The bass clef (also F clef) looks like a stylised 'F' and places the pitch F3 on the second line from the top. It is used for low-pitched instruments, bass voice, left-hand piano parts, and bass guitar.</p>
		<p>Two great mnemonics from remembering the bass clef are: <b>G</b>ood <b>B</b>oys <b>D</b>eserve <b>F</b>ruit <b>A</b>lways and <b>A</b>ll <b>C</b>ows <b>E</b>at <b>G</b>rass.</p>
		<img src="content/clefs/images/bass_clef.png">
		<p><b>Figure 2: </b>A bass clef with pitches written.</p>
		<hr>
	</section>
	<section>
		<h2 id="alto">Alto Clef</h2>
		<p>The alto clef belongs to the C clef family and places middle C (C4) on the middle line. Formally used for alto voice, it is now mainly used for viola notation.</p>
		<img src="content/clefs/images/alto_clef.png">
		<p><b>Figure 3: </b>An alto clef with middle C.</p>
		<hr>
	</section>
	<section>
		<h2 id="tenor">Tenor Clef</h2>
		<p>The tenor clef belongs to the C clef family and places middle C (C4) on the second line from the top. It is almost obsolete, but still sometimes used for cello, tenor, trombone, and bassoon notation.</p>
		<img src="content/clefs/images/tenor_clef.png">
		<p><b>Figure 4: </b>A tenor clef with middle C.</p>
		<hr>
	</section>
	<section>
		<h2 id="soprano">Soprano Clef</h2>
		<p>The soprano clef belongs to the C clef family and places middle C (C4) on the first line from the bottom. It is now an obsolete clef formally used in mediaeval music and some works well into the 19th century.</p>
		<img src="content/clefs/images/soprano_clef.png">
		<p><b>Figure 5: </b>A soprano clef with middle C.</p>
		<hr>
	</section>
	<section>
		<h2 id="neutral">Neutral Clef</h2>
		<p>The neutral clef is used for percussion notation. Each line or space represents a percussion instrument rather than a pitch.</p>
		<img src="content/clefs/images/neutral_clef.png">
		<p><b>Figure 6: </b>A neutral clef.</p>
