	</section>
	<section>
		<h2 id="tablature">Tablature Clef</h2>
		<p>Tablature is a form of musical notation which is written as fingering positions rather than notes. It is highly adopted by music for guitar, ukulele, lute, and other stringed instruments.</p>
		<p>The number of lines in a tablature staff is not always five, but rather the number of strings or voices used for a particular instrument.</p>
