	<section>
		<h1 id="top">Articulations</h1>
		<hr>
		<p>An articulation is a symbol written above or below a <a href="notes">note</a> or <a href="chords">chord</a> which imposes a playing technique.</p>
		<hr>
	</section>
	<section>
		<h2 id="accent">Accent</h2>
		<p>An accent indicates to strike a note to make it loud. Accents may be used to produce a irregular rhythmic pattern (metre).</p>
		<img src="content/articulations/images/accent.png">
		<p><b>Figure 1: </b>Accents on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="staccato">Staccato</h2>
		<p>A staccato indicates to play a note for roughly half its time value.</p>
		<img src="content/articulations/images/staccato.png">
		<p><b>Figure 2: </b>Staccatos on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="tenuto">Tenuto</h2>
		<p>A tenuto indicates to play a note to its full time value, sometimes even longer.</p>
		<img src="content/articulations/images/tenuto.png">
		<p><b>Figure 3: </b>Tenutos on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="wedge">Wedge</h2>
		<p>A wedge indicates to play a note for a quarter of its time value.</p>
		<img src="content/articulations/images/wedge.png">
		<p><b>Figure 4: </b>Wedges on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="marcato">Marcato</h2>
		<p>A marcato indicates to strike a note to make it loud, louder than an <a href="articulations#accent">accent</a>. Marcatissimo is the superlative.</p>
		<img src="content/articulations/images/marcato.png">
		<p><b>Figure 5: </b>Marcatos on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="staccatissimo">Staccatissimo</h2>
		<p>A staccatissimo has the same application as a <a href="articulations#wedge">wedge</a>.</p>
		<img src="content/articulations/images/staccatissimo.png">
		<p><b>Figure 6: </b>Staccatissimos on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="fermata">Fermata</h2>
		<p>A fermata indicates to pause on a note and prolong its duration.</p>
		<img src="content/articulations/images/fermata.png">
		<p><b>Figure 7: </b>Fermatas on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="short_fermata">Short Fermata</h2>
		<p>A short fermata prolongs the duration of a note or rest for shorter than a regular <a href="articulations#fermata">fermata</a>.</p>
		<img src="content/articulations/images/short_fermata.png">
		<p><b>Figure 9: </b>Short fermatas on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="long_fermata">Long Fermata</h2>
		<p>A long fermata prolongs the duration of a note or rest for longer than a regular <a href="articulations#fermata">fermata</a>.</p>
		<img src="content/articulations/images/long_fermata.png">
		<p><b>Figure 8: </b>Long fermatas on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="open_harmonic">Open/Harmonic</h2>
		<p>An open/harmonic has various applications: for string instruments, it indicates that a natural harmonic is played; for brass instruments, the note is played open without lowering any valve or without mute; in organ notation, a pedal-note is played with the heel (when above the note, use right foot; when below the note, use left foot); and in percussion notation, the hi-hat is opened.</p>
		<img src="content/articulations/images/open_harmonic.png">
		<p><b>Figure 9: </b>Open/harmonics on two notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="close_mute">Close/Mute</h2>
		<p>A close/mute has various functions: for string instruments, it indicates to pluck a note with the fret-hand; for the horn, the note is stopped by placing the stopping-hand into the bell; and in percussion notation, the hi-hat is closed.</p>
		<img src="content/articulations/images/close_mute.png">
		<p><b>Figure 10: </b>Close/mutes on two notes.</p>
		<hr>
	</section>
