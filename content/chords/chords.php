		<h1 id="top">Chords</h1>
		<hr>
		<p>A chord is the combination of two or more, but usually three or more <a href="notes">notes</a> played simultaneously.</p>
		<p>Chords are the basic foundation of musical harmony.</p>
		<img src="content/chords/images/chord.png">
		<p><b>Figure 1: </b>A C major chord.</p>
		<p>To build any chord, a basic understanding of <a href="intervals">intervals</a> is necessary.</p>
		<p>When a note within a chord is altered by an <a href="accidentals">accidental</a>, a new chord quality is imposed:</p>
		<img src="content/chords/images/major_becomes_minor.png">
		<p><b>Figure 2: </b>A major triad becomes minor by flattening the third.</p>
