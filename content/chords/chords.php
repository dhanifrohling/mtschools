	<section>
		<h2 id="inversions">Chord Inversions</h2>
		<p>A chord inversion (also slash chord) is the rearranging of notes within a given chord.</p>
		<p>Notes can only be strictly moved up or down an octave to impose a new chord inversion.</p>

		<p><b>Root Position</b></p>
		<p>Root position has the tonic note in the bass of a chord:</p>
		<img src="content/chords/images/root_position.png">
		<p><b>Figure 3: </b>Three different chords in root position.</p>

		<p><b>1st Inversion</b></p>
		<p>1st inversion has the note above the tonic in the bass of a chord:</p>
		<img src="content/chords/images/first_inversion.png">
		<p><b>Figure 4: </b>Three different chords in 1st inversion.</p>

		<p><b>2nd Inversion</b></p>
		<p>2nd inversion has the second note above the tonic in the bass of a chord:</p>
		<img src="content/chords/images/second_inversion.png">
		<p><b>Figure 5: </b>Two different chords in 2nd inversion.</p>

		<p><b>3rd Inversion</b></p>
		<p>3rd inversion has the third note above the tonic in the bass of a chord:</p>
		<img src="content/chords/images/third_inversion.png">
		<p><b>Figure 6: </b>A seventh chord in 3rd inversion.</p>

		<p>It is possible to do greater than 3rd inversion for chords that contain more than four notes (i.e. 9th, 11th, and 13th chords).</p>
