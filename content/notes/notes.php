	<section>
		<h1 id="top">Notes</h1>
		<hr>
		<p>A note is a single sound of music with the parameters pitch and duration.</p>
		<p>They are made up of three different components: a <b>notehead</b>, <b>stem</b>, and <b>flag</b>.</p>
		<p>In American parlance, a note is called a tone.</p>
		<img src="content/notes/images/notes.png">
		<p><b>Figure 1: </b>Five basic notes.</p>
		<hr>
	</section>
	<section>
		<h2 id="notehead">Notehead</h2>
		<p>A notehead is the centre-point of a note which indicates which pitch is played by it's position on the staff.</p>
		<p>Noteheads may also indicate a certain technique or instrument to be played.</p>
		<img src="content/notes/images/notehead.png">
		<p><b>Figure 2: </b>The notehead outlined in a single note.</p>
		<hr>
	</section>
	<section>
		<h2 id="stem">Stem</h2>
		<p>A stem is the vertical line of a note which links the notehead and tail together.</p>
		<img src="content/notes/images/stem.png">
		<p><b>Figure 3: </b>The stem outlined in a single note.</p>
		<p>Not all notes have a stem.</p>
		<hr>
	</section>
	<section>
		<h2 id="tail">Tail</h2>
		<p>A tail (or flag) is one or more curled lines at the end of a stem which indicates the <a href="noteandrestvalues">time value</a> of a note.</p>
		<img src="content/notes/images/tail.png">
		<p><b>Figure 4: </b>The tail outlined in a single note.</p>
		<p>Not all notes have a tail.</p>
