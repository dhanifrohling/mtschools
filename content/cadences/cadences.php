	<section>
		<h1 id="top">Cadences</h1>
		<hr>
		<p>A cadence (or close) is a harmonic or melodic progression which ends a <a href="phrase">phrase</a>, section, or composition.</p>
		<p>There are five different cadences: <b>perfect authentic</b>, <b>imperfect authentic</b>, <b>plagal</b>, <b>imperfect</b>, and <b>interrupted</b>.</p>
		<hr>
	</section>
